package MyPractice;

public class Expressions {

	public static void main(String[] args) {
//		expressions = operands & operators
//		operands = values, variables, numbers, quantity
//		operators = + * - / %
	
		int friends = 10;
		friends = friends + 1;
		System.out.println(friends); // 11
		friends = friends - 2;
		System.out.println(friends); // 9
		friends = friends * 2;
		System.out.println(friends); // 18
		friends = friends / 3;
		System.out.println(friends); // 6
		friends = friends % 2;
		System.out.println(friends); // 0
	} 

}
